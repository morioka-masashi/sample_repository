<?php
// 以下をfor文を使用して表示してください。
// ヒント: forの中でforを３回使用

// ********1
// *******121
// ******12321
// *****1234321
// ****123454321
// ***12345654321
// **1234567654321
// *123456787654321
// 12345678987654321

for ($y = 1; $y <= 9; $y++) {
  for ($x = 9; $x > $y; $x--) {
    echo "*";
  }
  for ($x = 1; $x < $y; $x++) {
    echo $x;
  }
  for ($x = $y; $x >= 1; $x--) {
    echo $x;
  }
  echo "<br>";
}